import math

# Ввод значений x и y
x = float(input("Введите число x: "))
y = float(input("Введите число y: "))

# Проверка, одного ли знака числа
if (x > 0 and y > 0) or (x < 0 and y < 0):
    # Среднее геометрическое (если числа одного знака)
    geometric_mean = math.sqrt(abs(x * y))
    print("Числа одного знака. Среднее геометрическое:", geometric_mean)
else:
    # Среднее арифметическое (если числа разных знаков или одно из них — ноль)
    arithmetic_mean = (x + y) / 2
    print("Числа разного знака или одно из них — ноль. Среднее арифметическое:", arithmetic_mean)
