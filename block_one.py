# Ввод сторон треугольника
x = float(input("Введите длину стороны x: "))
y = float(input("Введите длину стороны y: "))
z = float(input("Введите длину стороны z: "))

# Сортируем стороны, чтобы проверить гипотенузу как самую длинную
sides = [x, y, z]
sides.sort()
a, b, c = sides  # a и b — катеты, c — гипотенуза (предположительно)

# Проверяем условие прямоугольного треугольника по теореме Пифагора
if abs(c**2 - (a**2 + b**2)) < 1e-7:  # учитываем возможные погрешности с плавающей точкой
    # Вычисляем площадь
    area = (a * b) / 2
    print("Прямоугольный треугольник существует.")
    print("Площадь треугольника:", area)
else:
    print("Прямоугольный треугольник с такими сторонами не существует.")
